DROP DATABASE IF EXISTS `chickenjoy`;
CREATE DATABASE `chickenjoy` DEFAULT CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci;
USE `chickenjoy`;

CREATE TABLE `categories` (
  `id` INT(11) NOT NULL AUTO_INCREMENT,
  `category_code` VARCHAR(20) NOT NULL UNIQUE, 
  `category_name` VARCHAR(255) NOT NULL,      
  `description` TEXT,                       
  `status` ENUM('active', 'hidden') DEFAULT 'active',
  PRIMARY KEY (`id`)
) ENGINE=InnoDB;

CREATE TABLE `users` (
  `id` INT(11) NOT NULL AUTO_INCREMENT,
  `fullname` VARCHAR(255) NOT NULL,
  `email` VARCHAR(191) NOT NULL,
  `password` VARCHAR(255) NOT NULL,
  `phone` VARCHAR(15) NOT NULL,
  `gender` ENUM('nam', 'nu', 'khac') DEFAULT 'khac',
  `address` TEXT NOT NULL,
  `role` ENUM('user', 'admin') DEFAULT 'user',
  `points` INT(11) DEFAULT 0,
  `status` ENUM('active', 'locked') DEFAULT 'active',
  `reset_token` VARCHAR(255) DEFAULT NULL,
  `reset_expired` DATETIME DEFAULT NULL,
  `created_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  `updated_at` TIMESTAMP NULL ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY (`id`),
  UNIQUE KEY `email` (`email`),
  UNIQUE KEY `phone` (`phone`) -- Thêm unique số điện thoại để chặn trùng
) ENGINE=InnoDB;

CREATE TABLE `promotions` (
  `id` int(11) NOT NULL AUTO_INCREMENT,
  `code` varchar(50) NOT NULL UNIQUE, -- THÊM UNIQUE KHÓA NGOẠI
  `name` varchar(255) NOT NULL,
  `discount_percent` int(11) NOT NULL,
  `start_date` date NOT NULL,
  `end_date` date NOT NULL,
  `status` varchar(20) DEFAULT 'active',
  PRIMARY KEY (`id`)
) ENGINE=InnoDB;

CREATE TABLE `import_receipts` (
  `id` INT(11) NOT NULL AUTO_INCREMENT,
  `receipt_code` VARCHAR(20) NOT NULL UNIQUE,  
  `import_date` DATE NOT NULL,
  `total_amount` DECIMAL(15, 2) DEFAULT 0.00,  
  `status` ENUM('pending', 'completed') DEFAULT 'pending',
  PRIMARY KEY (`id`)
) ENGINE=InnoDB;

CREATE TABLE `products` (
  `id` int(11) NOT NULL AUTO_INCREMENT,
  `product_code` varchar(20) UNIQUE DEFAULT NULL,  
  `category_id` int(11) DEFAULT 1,                 
  `product_name` varchar(255) NOT NULL,            
  `description` text DEFAULT NULL,                 
  `unit` varchar(50) NOT NULL DEFAULT 'Phần',      
  `import_price` decimal(15, 2) DEFAULT 0.00,      
  `profit_rate` decimal(5, 2) DEFAULT 0.20,        
  `price` decimal(15, 2) NOT NULL,                 
  `stock` int(11) DEFAULT 0,                       
  `calories` int(11) DEFAULT 0,                    
  `protein` int(11) DEFAULT 0,                     
  `carbs` int(11) DEFAULT 0,                       
  `fat` int(11) DEFAULT 0,                         
  `image` varchar(255) DEFAULT 'default.jpg',      
  `is_new` tinyint(1) DEFAULT 0,                   
  `status` ENUM('active', 'hidden') DEFAULT 'active',
  `created_at` timestamp NOT NULL DEFAULT current_timestamp(),
  PRIMARY KEY (`id`),
  FOREIGN KEY (`category_id`) REFERENCES `categories`(`id`) ON DELETE RESTRICT
) ENGINE=InnoDB;

CREATE TABLE `import_receipt_details` (
  `id` INT(11) NOT NULL AUTO_INCREMENT,
  `receipt_id` INT(11) NOT NULL,               
  `product_id` INT(11) NOT NULL,                
  `quantity` INT(11) NOT NULL,                  
  `import_price` DECIMAL(15, 2) NOT NULL,      
  `subtotal` DECIMAL(15, 2) GENERATED ALWAYS AS (`quantity` * `import_price`) STORED,
  PRIMARY KEY (`id`),
  FOREIGN KEY (`receipt_id`) REFERENCES `import_receipts`(`id`) ON DELETE CASCADE,
  FOREIGN KEY (`product_id`) REFERENCES `products`(`id`) ON DELETE RESTRICT
) ENGINE=InnoDB;


CREATE TABLE `orders` (
  `id` INT PRIMARY KEY AUTO_INCREMENT,
  `user_id` INT NOT NULL,
  `receiver_name` VARCHAR(255) NOT NULL, 
  `total_price` DECIMAL(15, 2) NOT NULL,
  `discount_amount` DECIMAL(15, 2) DEFAULT 0.00,
  `promo_code` VARCHAR(50) DEFAULT NULL,
  `order_date` DATETIME DEFAULT CURRENT_TIMESTAMP,
  `status` ENUM('pending','confirmed','delivered','cancelled') DEFAULT 'pending',
  `payment_method` ENUM('cod', 'banking', 'online') DEFAULT 'cod',
  `address` TEXT NOT NULL,                
  `ward` VARCHAR(255) DEFAULT NULL,
  `phone` VARCHAR(20) NOT NULL,           
  `order_note` TEXT,                      
  FOREIGN KEY (`user_id`) REFERENCES `users`(`id`),
  FOREIGN KEY (`promo_code`) REFERENCES `promotions`(`code`) ON UPDATE CASCADE ON DELETE SET NULL -- THÊM KHÓA NGOẠI
) ENGINE=InnoDB;

CREATE TABLE `order_details` (
  `id` INT PRIMARY KEY AUTO_INCREMENT,
  `order_id` INT NOT NULL,
  `product_id` INT NOT NULL,
  `quantity` INT NOT NULL,
  `price_at_purchase` DECIMAL(15, 2) NOT NULL,
  FOREIGN KEY (`order_id`) REFERENCES `orders`(`id`) ON DELETE CASCADE, -- THÊM XÓA DÂY CHUYỀN
  FOREIGN KEY (`product_id`) REFERENCES `products`(`id`)
) ENGINE=InnoDB;

CREATE TABLE `point` (
  `id` INT(11) NOT NULL AUTO_INCREMENT,
  `user_id` INT(11) NOT NULL,
  `order_id` INT(11) DEFAULT NULL,
  `points_change` INT(11) NOT NULL,
  `reason` VARCHAR(255) NOT NULL,   
  `created_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (`id`),
  FOREIGN KEY (`user_id`) REFERENCES `users`(`id`) ON DELETE CASCADE,
  FOREIGN KEY (`order_id`) REFERENCES `orders`(`id`) ON DELETE SET NULL
) ENGINE=InnoDB;

CREATE TABLE `cart` (
    `id` INT PRIMARY KEY AUTO_INCREMENT,
    `user_id` INT NOT NULL,
    `product_id` INT NOT NULL,
    `quantity` INT NOT NULL DEFAULT 1,
    `created_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (`user_id`) REFERENCES `users`(`id`) ON DELETE CASCADE,
    FOREIGN KEY (`product_id`) REFERENCES `products`(`id`) ON DELETE CASCADE
) ENGINE=InnoDB;

